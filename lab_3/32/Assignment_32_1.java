public class Assignment_32_1 {
    public static void printStarRow(int n) {
        for (int i = 1; i <= n; i++) {
            System.out.print("*");
        }
        System.out.println();
        
    }

    public static void main(String[] args) {
        
        for (int i =1;i<=10;i++ ){
            printStarRow(i); 
        }
          
    }

}
