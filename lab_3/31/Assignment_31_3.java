public class Assignment_31_3 {
    public static void main(String[] args) {
        int n = Integer.parseInt(args[0]);
        System.out.println("n = " + n);
        for (int i = 1; i <= n; i++) {
            for (int x = 1; x <= x; x++) {
                System.out.print(i+1);
            }
            System.out.println();
        }
    }
}
